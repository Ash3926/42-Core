int	ft_isalnum(char *str)
{
	while (*str)
	{
		if ((*str > 64 && *str < 91) || (*str > 96 && *str < 123)
			|| (*str > 47 && *str < 58))
			str++;
		else
			return (0);
	}
	return (1);
}
