int	isascii(unsigned char *c)
{
	int	len;

	len = 0;
	while (*c)
	{
		len++;
		if (len > 7)
			return (0);
		if (*c >= 0 && *c < 128)
			c++;
		else
			return (0);
	}
	if (len == 7)
		return (1);
	return (0);
}
